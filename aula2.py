# comando print e paramentos 

# por padrao o end é uma quebra de linhas mas nos nao vemos,
# ele ja faz isso automatico mas com os parametros podemos personalizar isso.

# \n quebra de linha esse comando que roda quando escrevemos um print em baixo do outros mas sem nos vermos 

# sep= separador podemos alterar a forma em que os argumentos sao separados
# end= podemos mudar o final para fazer outra coisa ao inves da quebra de linha automatica 
print (12, 34, 56, sep='--', end='\n ##')
print (78, 9, 10, sep='--', end='\n ##')
